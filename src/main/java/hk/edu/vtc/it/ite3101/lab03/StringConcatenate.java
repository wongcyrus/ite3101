package hk.edu.vtc.it.ite3101.lab03;

public class StringConcatenate {
    public static void main(String[] args) {
        int number1 = 200;
        int number2 = 8;
        int number3 = 18;
        System.out.println("Next year is " + number1 + number2);
        System.out.println("My age is " + (number2 + number3));
    }
}